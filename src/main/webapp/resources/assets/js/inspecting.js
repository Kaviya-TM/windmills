$('#1w').keyup(function(){
	var a;
	var b;
	var c;
	var d;
	var e;
	if($(this).val() === ""){
		e = 0;
	}
	else{
		e = $(this).val();
	}
	if($('#2w').val() === ""){
		a = 0;
	}
	else{
		a= $('#2w').val();
	}
	if($('#3w').val() === ""){
		b = 0;
	}
	else{
		b= $('#3w').val();
	}
	if($('#4w').val() === ""){
		c = 0;
	}
	else{
		c= $('#4w').val();
	}
	if($('#5w').val() === ""){
		d = 0;
	}
	else{
		d= $('#5w').val();
	}
	waresize =parseInt(e) + parseInt(a) + parseInt(b) + parseInt(c) + parseInt(d);
	$('#fw').val(toComma(waresize));
	totalsize = parseInt(waresize) + parseInt(laboursize) + parseInt(officesize) + parseInt(roomsize);
	$('#ts').val(toComma(totalsize));
});
