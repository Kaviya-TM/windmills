$('#grossinc').keyup(function(){
	var a = $(this).val();
	var bua = $('#buitUpAreaSize').val();
	var aa = a / bua;
	$('#grossincbua').val(toComma(aa));
	var b = ($('#ope').val()).replace(/%/g , '') * a / 100;
	$('#opemv').val(toComma(b));
	var bb = b / bua;
	$('#opebua').val(toComma(bb));
	var c = ($('#vc').val()).replace(/%/g , '') * a / 100;
	$('#vcmv').val(toComma(c));
	var cc = c / bua;
	$('#vcbua').val(toComma(cc));
	var d = ($('#llc').val()).replace(/%/g , '') * a / 100;
	$('#llcmv').val(toComma(d));
	var dd = d / bua;
	$('#llcbua').val(toComma(dd));
	var e = a - b - c - d ;
	$('#netinc').val(toComma(e));
	var ee = e / bua;
	$('#netbua').val(toComma(ee));
	var f = e / ($('#netyield').val()).replace(/%/g , '')  * 100;
	$('#emv').val(toComma(f));
	var ff = f / bua;
	$('#ebua').val(toComma(ff)); 
	var h = parseInt(a) / parseInt(f) * 100; 
	$('#grossyield').val(h.toFixed(2) + "%");
});
